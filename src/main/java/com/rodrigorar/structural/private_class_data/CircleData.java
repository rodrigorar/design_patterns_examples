package com.rodrigorar.structural.private_class_data;

class CircleData {
	private int x;
	private int y;
	private int radius;

	CircleData(int x, int y, int radius) {
		this.x = x;
		this.y = y;
		this.radius = radius;
	}

	int getX() {
		return x;
	}

	int getY() {
		return y;
	}

	int getRadius() {
		return radius;
	}
}
